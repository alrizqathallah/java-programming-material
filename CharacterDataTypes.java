public class CharacterDataTypes {
  public static void main(String[] args) {
    // Character Data Types.
    /*
      - Data karakter atau huruf direpresentasikan dengan 'char'.
      - Untuk membuat tipe data karakter, kita dapat menggunakan petik satu ('').
     */

     char B = 'B';
     char E = 'E';
     char N = 'N';
     char K = 'K';
     char Z = 'Z';

     System.out.println(B);
     System.out.println(E);
     System.out.println(N);
     System.out.println(K);
     System.out.println(Z);

     System.out.print(B);
     System.out.print(E);
     System.out.print(N);
     System.out.print(K);
     System.out.print(Z);
  }
}
