public class BooleanDataTypes {
  public static void main(String[] args) {
    // Boolean Data Types
    /*
      - Tipe data yang memiliki nilai 2 nilai, yaitu benar dan salah.
      - Tipe data boolean di Java direpresentasikan dengan kata kunci 'boolean'.
      - Nilai benar direpresentasikan dengan 'true'.
      - Nilai salah direpresentasikan dengan 'false'.
      - Default value untuk boolean adalah false.
     */

     boolean True = true;
     boolean False = false;

     System.out.println(True);
     System.out.println(False);
  }
}
